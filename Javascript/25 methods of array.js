let fruits=["apple","mango","banana","grapes",1,2,3,null,undefined]
console.log(fruits)

//push - add at last
fruits.push("kiwi")
console.log(fruits)

//pop - remove from last
fruits.pop();
console.log(fruits)

//unshift-add on starting
fruits.unshift("guava")
console.log(fruits)

//shift -remove from starting
let removedfruit=fruits.shift()
console.log(removedfruit)
console.log(fruits)